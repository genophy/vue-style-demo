                    wheels different letters, and hence different messages, could be produced. In effect, it could be mechanically "programmed" to read instructions. Along with two other complex
                    machines, the doll is at the Musée d'Art et d'Histoire of Neuchâtel, Switzerland, and still operates.[14]

                    The tide-predicting machine invented by Sir William Thomson in 1872 was of great utility to navigation in shallow waters. It used a system of pulleys and wires to automatically
                    calculate predicted tide levels for a set period at a particular location.

