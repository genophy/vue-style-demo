                    Lord Kelvin had already discussed the possible construction of such calculators, but he had been stymied by the limited output torque of the ball-and-disk integrators.[15] In a
                    differential analyzer, the output of one integrator drove the input of the next integrator, or a graphing output. The torque amplifier was the advance that allowed these machines
                    to work. Starting in the 1920s, Vannevar Bush and others developed mechanical differential analyzers.
